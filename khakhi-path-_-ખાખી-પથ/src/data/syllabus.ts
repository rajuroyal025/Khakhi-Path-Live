export function getDailyFocus(date: Date) {
  const dayOfYear = Math.floor((date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000);
  
  const conIdx = dayOfYear % syllabus.constitution.length;
  const sciIdx = dayOfYear % syllabus.science.length;
  const histIdx = dayOfYear % syllabus.history.length;
  const geoIdx = dayOfYear % syllabus.geography.length;

  return {
    constitution: syllabus.constitution[conIdx],
    science: syllabus.science[sciIdx],
    history: syllabus.history[histIdx],
    geography: syllabus.geography[geoIdx]
  };
}
